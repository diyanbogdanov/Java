package floatordouble;

public class FloatOrDouble {

    public static void main(String[] args) {
        float firstVariable = 12.345f;
        float secondVariable = 3456.091f;
        double thirdVariable = 34.567839023;
        double forthVariable = 8923.1234857;
        
        System.out.printf("Floats are: %.3f & %.3f\nDoubles are: %.9f & %.7f", firstVariable, secondVariable, thirdVariable, forthVariable);
    }
    
}
